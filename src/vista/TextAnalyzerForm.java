    private void jMenuItemBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemBuscarActionPerformed
        // TODO add your handling code here:
String palabra = JOptionPane.showInputDialog(this, "Ingrese la palabra a buscar:");
    int posicion = textEditorHandler.findText(jTextArea1, palabra);
    if (posicion != -1) {
        jTextArea1.requestFocus();
        jTextArea1.select(posicion, posicion + palabra.length());
    } else {
        JOptionPane.showMessageDialog(this, "Palabra no encontrada.");
    }
        
    }//GEN-LAST:event_jMenuItemBuscarActionPerformed

