    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        // Obtener el texto del JTextArea
    String text = jTextArea1.getText();
    
// Crear una instancia de StringAnalyzer
    StringAnalyzer analyzer = new StringAnalyzer(text);

    // Calcular la longitud del texto
    int length = text.length();
    // Actualizar el JLabel con la longitud del texto
    jLabel1.setText("Longitud del texto: " + length);

    // Usar StringAnalyzer para contar palabras, obtener la primera y la última palabra
    int totalWords = analyzer.countWords();
    String firstWord = analyzer.getFirstWord();
    String lastWord = analyzer.getLastWord();
    char firstLetter = analyzer.getFirstLetter();
    char lastLetter = analyzer.getLastLetter();
    char middleLetter = analyzer.getMiddleLetter();
    String centralWord = analyzer.getCentralWord();

    // Mostrar los resultados en los JLabel correspondientes
    jLabel4.setText("Total de palabras: " + totalWords);    
    jLabel7.setText("Primera palabra: " + firstWord);
    jLabel9.setText("Última palabra: " + lastWord);    
    jLabel5.setText("Primera letra: " + firstLetter);
    jLabel6.setText("Última letra: " + lastLetter);
    jLabel10.setText("Letra del centro: " + middleLetter);
    jLabel8.setText("Palabra central: " + centralWord);    

// Crear una instancia de StringBuilderAnalyzer
    StringBuilderAnalyzer builderAnalyzer = new StringBuilderAnalyzer(text);

     // Usar StringBuilderAnalyzer para contar repeticiones y palabras pares/impares
    int countA = builderAnalyzer.countA();
    int countE = builderAnalyzer.countE();
    int countI = builderAnalyzer.countI();
    int countO = builderAnalyzer.countO();
    int countU = builderAnalyzer.countU();
    int evenWords = builderAnalyzer.countEvenLengthWords();
    int oddWords = builderAnalyzer.countOddLengthWords();

    // Mostrar los resultados en los JLabel correspondientes
    jLabel11.setText("Repeticiones de 'A', 'a', 'á': " + countA);
    jLabel12.setText("Repeticiones de 'E', 'e', 'é': " + countE);
    jLabel13.setText("Repeticiones de 'I', 'i', 'í': " + countI);
    jLabel14.setText("Repeticiones de 'O', 'o', 'ó': " + countO);
    jLabel15.setText("Repeticiones de 'U', 'u', 'ú': " + countU);
    jLabel16.setText("Palabras con cantidad de caracteres par: " + evenWords);
    jLabel17.setText("Palabras con cantidad de caracteres impar: " + oddWords);


    // Traduccion a murcielago
    // Obtener el texto del JTextArea1
    //String text = jTextArea1.getText();

    // Crear una instancia de TextEditorHandler si no lo has hecho antes
    TextEditorHandler textEditorHandler = new TextEditorHandler();

    // Traducir el texto usando el método translateToMurcielago
    String translatedText = textEditorHandler.translateToMurcielago(text);

    // Mostrar el texto traducido en jTextArea2
    jTextArea2.setText(translatedText);
    
    
    }//GEN-LAST:event_jButton1ActionPerformed

